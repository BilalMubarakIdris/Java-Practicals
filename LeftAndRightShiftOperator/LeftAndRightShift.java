package LeftAndRightShiftOperator;

public class LeftAndRightShift {
    public static void main(String[] args) {
        // Left Shift and Right Shift Operators
        // << >>

        int a = 25; // 1 1 0 0 1
        int b = a << 2; // 1 1 0 -> 6
        // int b = a << 2; // 1 1 0 -> 100
        System.out.println(b);
    }
}
